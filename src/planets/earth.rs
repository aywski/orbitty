// Sample the map at up to 4 nearby points and blend - gives smoother coast.
fn land_coverage(lat: f64, lon: f64) -> f64 {
    let lon_w = wrap_lon(lon);
    let fx = (lon_w + PI) / (2.0 * PI) * 72.0;
    let fy = (PI / 2.0 - lat) / PI * 36.0;

    let x0 = (fx.floor() as i64).clamp(0, 71) as usize;
    let y0 = (fy.floor() as i64).clamp(0, 35) as usize;
    let x1 = (x0 + 1).min(71);
    let y1 = (y0 + 1).min(35);
    let tx = fx - fx.floor();
    let ty = fy - fy.floor();

    let v00 = (MAP[y0 * 72 + x0] == b'1') as i32 as f64;
    let v10 = (MAP[y0 * 72 + x1] == b'1') as i32 as f64;
    let v01 = (MAP[y1 * 72 + x0] == b'1') as i32 as f64;
    let v11 = (MAP[y1 * 72 + x1] == b'1') as i32 as f64;

    let a = v00 + tx * (v10 - v00);
    let b = v01 + tx * (v11 - v01);
    a + ty * (b - a)
}

fn is_land(lat: f64, lon: f64) -> bool {
    let cov = land_coverage(lat, lon);
    // Add fractal coastline noise in the transition zone.
    if cov > 0.85 { return true; }
    if cov < 0.15 { return false; }
    let n = (noise2(lon * 6.0, lat * 6.0) - 0.5) * 0.35
          + (noise2(lon * 14.0, lat * 14.0) - 0.5) * 0.15;
    cov + n > 0.5
}

impl Planet for Earth {
    fn surface_color(&self, lat: f64, lon: f64) -> Rgb {
        let abs_lat = lat.abs();

        if abs_lat > 1.42 {
            return Rgb(240, 248, 255);
        }

        let polar_blend = if abs_lat > 1.20 {
            ((abs_lat - 1.20) / 0.22).clamp(0.0, 1.0).powf(1.2)
        } else {
            0.0
        };

        let base = if is_land(lat, lon) {
            land_color(lat, lon)
        } else {
            ocean_color(lat, lon)
        };

        let clouds = cloud_layer(lat, lon);
        let with_clouds = base.lerp(Rgb(245, 248, 252), clouds);

        if polar_blend > 0.0 {
            with_clouds.lerp(Rgb(240, 248, 255), polar_blend)
        } else {
            with_clouds
        }
    }
}

fn land_color(lat: f64, lon: f64) -> Rgb {
    let abs_lat = lat.abs();
    let n = noise2(lon * 3.0, lat * 3.0) * 0.5
          + noise2(lon * 9.0, lat * 9.0) * 0.35
          + noise2(lon * 22.0, lat * 22.0) * 0.15;

    let lat_d = lat.to_degrees();

    // Desert belts ~15-33° latitude (Sahara, Arabia, Gobi, Australian outback).
    if lat_d.abs() > 13.0 && lat_d.abs() < 34.0 {
        let d = noise2(lon * 1.5, lat * 2.0);
        if d > 0.35 {
            let sand = Rgb(215, 185, 118);
            let dune = Rgb(185, 150, 80);
            let rock = Rgb(155, 120, 72);
            return if n > 0.65 { sand.lerp(dune, (n - 0.65) / 0.35) }
                   else { rock.lerp(sand, n / 0.65) };
        }
    }

    if abs_lat < 0.22 {
        // Deep tropics - rainforest
        Rgb(22, 88, 28).lerp(Rgb(48, 115, 38), n)
    } else if abs_lat < 0.52 {
        // Subtropics/tropics
        Rgb(55, 115, 42).lerp(Rgb(80, 140, 55), n)
    } else if abs_lat < 0.90 {
        // Temperate - grass, forest, farmland
        let grass = Rgb(90, 130, 50);
        let forest = Rgb(45, 90, 32);
        let farm = Rgb(130, 130, 68);
        if n > 0.65 { grass.lerp(farm, (n - 0.65) / 0.35) }
        else { forest.lerp(grass, n / 0.65) }
    } else if abs_lat < 1.18 {
        // Boreal / taiga
        Rgb(38, 72, 38).lerp(Rgb(60, 90, 48), n)
    } else {
        // Tundra
        Rgb(112, 108, 94).lerp(Rgb(148, 138, 118), n)
    }
}

fn ocean_color(lat: f64, lon: f64) -> Rgb {
    let abs_lat = lat.abs();
    let n = noise2(lon * 4.0, lat * 4.0) * 0.4
          + noise2(lon * 11.0, lat * 11.0) * 0.2;
    let t = (abs_lat / (PI / 2.0)).clamp(0.0, 1.0);
    let tropical = Rgb(28, 88, 155);
    let cold = Rgb(15, 42, 92);
    let base = tropical.lerp(cold, t * t);
    base.lerp(Rgb(48, 115, 180), n * 0.45)
}

fn cloud_layer(lat: f64, lon: f64) -> f64 {
    let c1 = noise2(lon * 2.5 + 100.0, lat * 2.5 + 50.0);
    let c2 = noise2(lon * 6.0 + 10.0, lat * 6.0 - 20.0);
    let c = (c1 * 0.7 + c2 * 0.3 - 0.55).max(0.0);
    let itcz = (-(lat * lat) * 4.0).exp() * 0.12;
    (c * 0.85 + itcz).clamp(0.0, 0.70)
}
