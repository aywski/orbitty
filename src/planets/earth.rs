// Sample the map at up to 4 nearby points and blend - gives smoother coast.
fn land_coverage(lat: f64, lon: f64) -> f64 {
    let lon_w = wrap_lon(lon);
    let fx = (lon_w + PI) / (2.0 * PI) * 72.0;
    let fy = (PI / 2.0 - lat) / PI * 36.0;

    let x0 = (fx.floor() as i64).clamp(0, 71) as usize;
    let y0 = (fy.floor() as i64).clamp(0, 35) as usize;
    let x1 = (x0 + 1).min(71);
    let y1 = (y0 + 1).min(35);
    let tx = fx - fx.floor();
    let ty = fy - fy.floor();

    let v00 = (MAP[y0 * 72 + x0] == b'1') as i32 as f64;
    let v10 = (MAP[y0 * 72 + x1] == b'1') as i32 as f64;
    let v01 = (MAP[y1 * 72 + x0] == b'1') as i32 as f64;
    let v11 = (MAP[y1 * 72 + x1] == b'1') as i32 as f64;

    let a = v00 + tx * (v10 - v00);
    let b = v01 + tx * (v11 - v01);
    a + ty * (b - a)
}

fn is_land(lat: f64, lon: f64) -> bool {
    let cov = land_coverage(lat, lon);
    // Add fractal coastline noise in the transition zone.
    if cov > 0.85 {
        return true;
    }
    if cov < 0.15 {
        return false;
    }
    let n =
        (noise2(lon * 6.0, lat * 6.0) - 0.5) * 0.35 + (noise2(lon * 14.0, lat * 14.0) - 0.5) * 0.15;
    cov + n > 0.5
}

impl Planet for Earth {
    fn moons(&self) -> &[Moon] {
        static M: [Moon; 1] = [Moon {
            color: Rgb(188, 183, 170),
            radius: 0.08,
            orbital_radius: 2.8,
            inclination: 0.09,
            speed: 0.22,
            phase: 1.5,
        }];
        &M
    }

    fn surface_color(&self, lat: f64, lon: f64) -> Rgb {
        let base = if is_land(lat, lon) {
            land_color(lat, lon)
        } else {
            ocean_color(lat, lon)
        };

        let clouds = cloud_layer(lat, lon);
        base.lerp(Rgb(245, 248, 252), clouds)
    }
}

fn land_color(lat: f64, lon: f64) -> Rgb {
    let abs_lat_d = lat.abs().to_degrees();
    let n = noise2(lon * 3.0, lat * 3.0) * 0.7 + noise2(lon * 8.0, lat * 8.0) * 0.3;

    if abs_lat_d < 12.0 {
        // Equatorial rainforest
        Rgb(18, 80, 24).lerp(Rgb(42, 108, 34), n)
    } else if abs_lat_d < 25.0 {
        // Tropical - mostly green with slight dry tinge near 25°
        let t = (abs_lat_d - 12.0) / 13.0;
        Rgb(48, 108, 36).lerp(Rgb(95, 118, 48), t * (0.3 + n * 0.4))
    } else if abs_lat_d < 33.0 {
        // Semi-arid / savanna - muted, not strongly yellow
        let sand = Rgb(155, 138, 82);
        let dry_green = Rgb(98, 118, 52);
        dry_green.lerp(sand, n * 0.55)
    } else if abs_lat_d < 52.0 {
        // Temperate - green forest and grassland
        Rgb(42, 88, 30).lerp(Rgb(82, 122, 44), n)
    } else {
        // Boreal / taiga
        Rgb(35, 68, 35).lerp(Rgb(55, 85, 44), n)
    }
}

fn ocean_color(lat: f64, lon: f64) -> Rgb {
    let abs_lat = lat.abs();
    let n = noise2(lon * 4.0, lat * 4.0) * 0.4 + noise2(lon * 11.0, lat * 11.0) * 0.2;
    let t = (abs_lat / (PI / 2.0)).clamp(0.0, 1.0);
    let tropical = Rgb(28, 88, 155);
    let cold = Rgb(15, 42, 92);
    let base = tropical.lerp(cold, t * t);
    base.lerp(Rgb(48, 115, 180), n * 0.45)
}

fn cloud_layer(lat: f64, lon: f64) -> f64 {
    let c1 = noise2(lon * 2.5 + 100.0, lat * 2.5 + 50.0);
    let c2 = noise2(lon * 6.0 + 10.0, lat * 6.0 - 20.0);
    let c = (c1 * 0.7 + c2 * 0.3 - 0.55).max(0.0);
    let itcz = (-(lat * lat) * 4.0).exp() * 0.12;
    (c * 0.85 + itcz).clamp(0.0, 0.70)
}
